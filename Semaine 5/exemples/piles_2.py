# On utlise une liste pour simuler une pile
ma_pile = []

# On ajoute des éléments à la pile
ma_pile.append(1)
ma_pile.append(2)
ma_pile.append(3)

# On retire un élément de la pile
element_retire = ma_pile.pop()
print(element_retire)

# On retire un autre élément de la pile
element_retire = ma_pile.pop()
print(element_retire)

# Affiche la liste
print(ma_pile)
